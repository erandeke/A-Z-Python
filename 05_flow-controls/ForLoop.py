# For loop is an iterator that will iterate on the items step by step

# Print 0 to 9 in python

for x in range(10):
    print("Number is ", x);

# Program to calculate the sum of numbers

sum = 0;

list = [1, 2, 3, 4, 5];

for x in list:
    sum = sum + x;
print("Sum of numbers in the list", sum);

# Program to iterate through indexing

arr = ['kedar', 'erande'];
for i in range(len(arr)):
    print(arr[i]);
